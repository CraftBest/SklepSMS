						<h2><a href="#">Regulamin</a></h2>
			<p><strong><div style="text-align: center;font-size: 30px;">REGULAMIN SERWERA I FORUM CRAFTBEST.PL</div></strong></p><br /><br />

				<p><strong><em>§1 Postanowienia ogólne:</em></strong></p><br />

				<p>1. Każdy użytkownik serwisu craftbest.pl oraz serwerów craftbest.pl jest zobowiązany do przestrzegania niniejszego regulaminu<br />
				2. Gra na serwerach craftbest.pl jest darmowa.<br />
				3. Wszelkie błędy dotyczące forum lub serwera należy natychmiastowo zgłosić obsłudze.<br />
				4. Obsługa serwera ma prawo karać za czyny nie uwzględnione w regulaminie, które działają na szkodę rozrywki<br />
				5. Na naszych serwerach jest bezwzględny zakaz korzystania z oprogramowania ułatwiającego grę - tzw. "cheatów" <br />
				6. Nieznajomość regulaminu nie zwalnia z jego przestrzegania. <br />
				7. W całym serwisie craftbest.pl (serwer oraz forum) obowiązują ogólnie pojęta zasada dobrego wychowania, ograniczonego zaufania<br />
				8. Wszelkie dane zapisywane na naszych serwerach nie są nigdzie udostępniane ani odsprzedawane!</p><br />

				<p><strong><em>§2 Regulamin gry: </em></strong></p><br />

				<p>1. Na serwerze obowiązuje zakaz używania słów powszechnie uważanych za wulgarne, oraz obowiązuje zakaz obrażania innych graczy,<br />
				2. Zakazuje się wysyłania tej samej wiadomości kilkukrotnie (tj. "spamowania"<br />
				3. Natychmiastowym banem kończy się w jakikolwiek sposób reklamowanie innych serwerów - minecraft oraz innych gier.<br />
				4. Zakaz giftowania<br />
				5. Zakaz używania cheatów oraz modyfikacji na serwerze. ( optifine, Rei minimap itd.. są dozwolone ),<br />
				6. Każdy bug w grze musi być natychmiast zgłoszony obsłudze, a korzystanie z nich będzie traktowane jako korzystanie z cheatów.<br />
				7. Handel pomiędzy graczami odbywa się na własne ryzyko<br />
				8. Podawanie się za rodzine, znajomych lub osoby uprzywilejowane (np za brata administratora) jest zakazane.<br />
				9. Zakazane jest nękanie innych graczy oraz groźby karalne.<br />
				10. Zakaz używania komendy /gm na arenach PVP oraz eventach<br />
				11. Zakaz pisania do administracji na prywatnych wiadomościach.<br />
				12. Zakaz nagminnego pisania prośb do administracji o przedmioty, darmowe konta vip bądź OP,<br />
				13. Zakaz nadużywania caps locka.<br />
				14. Posiadanie przedmiotów, które posiadają enchant powyżej 8 uznawane jest za posiadanie cheatów i jest banowane.<br />
				15. Posiadanie przedmiotów stackowanych, których nie da się w sposób naturalny otrzymać jest traktowane jako posiadanie cheatów.<br />
				16. Wszelkie utrudnianie rozgrywki graczom jest zakazane<br />
				17. Obowiązuje zakaz rozrzucania darmowego ekwipunku na spawnie.<br />
				18. Kiedy oczekuje się pomocy od administratora należy poczekać na swoją kolej.<br />
				19. Każdy gracz na /warp działki może posiadać maksymalnie 3 działki.<br />
				20. Niestosowanie się do w/w reguły skutkuje odebraniem wszystkich działek.<br />
				21. Każdy gracz zobowiązany jest do przeczytania regulaminu danego warpa.<br />
				22. Administracja nie tworzy warpów na życzenie.<br />
				23. Użytkownicy do administracji zobowiązani są zwracać się uprzejmie.<br />
				24. Administracja nie odpowiada za utracone domy oraz ekwipunek.<br />
				25. Zakazuje się claimowania na warp sv na terenie spawnu (czerwona linia)  oraz tworzenia gildii na terenie działek.<br />
				26. Obowiązkiem jest zgłaszanie niewłaściwego zachowania innych użytkowników<br />
				27. Obowiązuje zakaz korzystania z komendy /fly na /warp sv<br />
				30. Administracja nie ponosi odpowiedzialności za konta użytkowników w razie ich utraty.</p><br />

				<p><strong><em>§3 Regulamin forum:</em></strong><br /><br />
				1. Na forum obowiązuje zakaz używania słów powszechnie uważanych za wulgarne, oraz obrażania innych użytkowników forum<br />
				2. Umieszczanie treści obraźliwych bądź pornograficznych skutkuje natychmiastowym zbanowaniem użytkownika<br />
				3. Zakaz zamieszczania treści niezgodnych z obowiązującym prawem Rzeczpospolitej Polski.<br />
				4. Zakaz zamieszczania treści dyskryminujących inne rasy, grupy społeczne oraz mniejszości narodowe.<br />
				5. Zakaz zamieszczania w nazwie użytkownika wulgarnych słów oraz, treści reklamujących inne serwery/strony.<br />
				6. Zakaz spamowania na forum (wysyłanie zbędnych, nic nie wnoszących do tematu postów).<br />
				7. Zakaz umieszczania linków do stron innych serwerów oraz reklamowania ich podając IP lub nazwę serwera,<br />
				8. Zakaz podawania się pod członka Administracji CraftBest.pl przez nieuprawnionych użytkowników.<br />
				9. Zakaz pisania "powody jak wyżej" itp.</p><br />

				<p><em><strong>§4 Zasady użytkowania konta VIP:</strong></em></p><br />

				<p>1. Konta VIP, SUPER VIP oraz SUPER ELITE VIP są kontami nieobowiązkowymi, za dodatkową opłatą.<br />
				2. Konta VIP posiadają specjalne, uprzywilejowane komendy, które ułatwiają użytkownikom grę.<br />
				3. Użytkownicy z kontami VIP nie mogą udostępniać przedmiotów z komend dostępnych dla ów użytkowników. Nieprzestrzeganie tej zasady karane jest banem, bądź odebraniem przywilejów i degradacją do konta "Gracz"<br />
				4. Użytkownik konta VIP ma zakaz korzystania z tzw. "gamemode"(kom. /gm) w miejscach, gdzie jest to zakazane. <br />
				5. Konta VIP są kontami stałymi, bezterminowymi (z wyjątkiem serwera PvP). <br />
				6. W przypadku naruszenia powyższego regulaminu użytkownik może utracić swoją rangę.<br />
				7. Koszt usługi jest jednorazowy. Nie ma możliwości zrezygnowania z usługi po jej zamówieniu.<br />
				8. Posiadanie konta VIP nie zwalnia z przestrzegania regulaminu.</p><br />

